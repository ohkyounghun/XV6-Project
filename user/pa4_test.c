static void
usage(void)
{
    printf("Usage: pa4_test <1|2|3|4> [fork_flag]\n");
    printf("  fork_flag: 0 = no fork (default), 1 = fork before work\n");
    printf("  (Test 4 ignores fork_flag; it manages its own fork.)\n");
}

int
main(int argc, char *argv[])
{
    int which, do_fork;

    if (argc < 2) {
        usage();
        exit(1);
    }

    which = atoi(argv[1]);
    do_fork = (argc >= 3) ? atoi(argv[2]) : 0;

    switch (which) {
    case 1: run_test1(do_fork); break;
    case 2: run_test2(do_fork); break;
    case 3: run_test3(do_fork); break;
    case 4: run_test4(do_fork); break;
    default:
        printf("pa4_test: invalid test number %d\n", which);
        usage();
        exit(1);
    }

    exit(0);
}
