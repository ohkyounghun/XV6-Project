// pa4_test.c
//
// Unified test dispatcher for PA4 (Page Replacement).
// Combines test1.c ~ test4.c into a single user program.
//
// Usage in xv6 shell:
//   pa4_test <which> [fork_flag]
//     which     : 1, 2, 3, or 4   -> selects scenario
//     fork_flag : 0 (default) or 1 -> if 1, fork before the workload
//                 (Test 4 ignores fork_flag; it forks internally for Part B)
//
// Behaviour:
//   - test1 : 1000 pages, 2000 read-only accesses (working-set pressure)
//   - test2 : 700 pages, write+verify with 5000 random checks
//   - test3 : 400-page hot/cold block + per-round sbrk-grown churn that
//             accumulates (200 pages/round x 5).  Drives swap each round
//             and exposes clock-vs-FIFO behavior in the read delta.
//   - test4 : Part A: sbrk-based OOM probe (in a forked child)
//             Part B: fork() with swapped-out pages, integrity check
//
// Notes:
//   - No PASS/FAIL judgement in code. Tests print numbers; TAs grade by review.
//   - Reference swap counts depend on PHYSTOP, scheduler, and prior shell
//     state; they should be re-measured per environment against a known-good
//     reference implementation.

