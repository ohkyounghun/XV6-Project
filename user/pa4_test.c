// ------------- test3: clock algorithm behavior -------------
//
// Layout:
//   hot   : [0, 200)   — re-touched at the start of every round (PTE_A=1)
//   cold  : [200, 400) — touched once at init, then ignored forever
//   churn : a separate heap region grown via sbrk(+CHURN_SZ) at the
//           start of every round.  Unlike user-level malloc/free
//           (which just bookkeeps inside the process heap), sbrk
//           actually changes the kernel's view of the process's memory.
//
//           We *accumulate* the churn rather than freeing it each round.
//           Reason: a sbrk(+n) followed by sbrk(-n) within the same
//           round returns the pages to the kernel free-list, so the
//           next round's sbrk(+n) silently reuses them without touching
//           swap.  Accumulating means every round really does demand
//           fresh pages, every round really does drain the free-list,
//           and every round really does drive swap_out.
//
//           At the end of the loop we release everything in one shot.
//
// Expected pattern:
//   * Correct Clock: hot stays resident (PTE_A protects it), so most
//     evictions hit cold or churn.  swap-in on hot is rare even across
//     many rounds.  Per-round delta stabilizes after round 1.
//   * Naive FIFO: hot has no protection; round 1 burns through hot,
//     so subsequent rounds need to swap hot back in -> per-round read
//     delta climbs sharply.
//
// NOTE: still requires fork_flag=1 to get above the PHYSTOP fit point.
//     pa4_test 3 1

#define NUM_PAGE_T3   400        // hot + cold (resident block)
#define T3_HOT_LO     0
#define T3_HOT_HI     200
#define T3_COLD_LO    200
#define T3_COLD_HI    400
#define T3_CHURN_SZ   200        // pages added each round (cumulative)
#define T3_ROUNDS     5          // total churn pressure: 5 * 200 = 1000 pages

static void
run_test3(int do_fork)
{
    int i, r;
    char *mem;
    char *churn;
    int x1, y1, x2, y2;
    int prev_x, prev_y;
    int child = 0;
    int wrong = 0;
    int sink = 0;

    mem = (char *)malloc(PAGE_SIZE * NUM_PAGE_T3);
    printf("zip_test_3\n");

    if (!do_fork) {
        printf("[note] running in single-process mode; "
               "swap activity may be low. retry with fork_flag=1.\n");
    }

    if (do_fork) {
        child = fork();
        if (child < 0) {
            printf("fork failed\n");
            free((void *)mem);
            return;
        }
        if (child > 0) {
            wait(0);
            free((void *)mem);
            return;
        }
        // Child falls through.
    }

    // Initialize the resident block (hot + cold) with known values so
    // the final integrity check has something to verify against.
    for (i = 0; i < NUM_PAGE_T3; i++)
        mem[PAGE_SIZE * i] = i % 10;

    // Snapshot before the rounds begin.  We use this to compute the
    // "this round" counts.
    swapstat(&x1, &y1);
    prev_x = x1;
    prev_y = y1;

    // Each round:
    //   1. Re-touch hot pages so the MMU sets PTE_A=1 on them.
    //   2. sbrk a CHURN-page block on top of the current heap and touch
    //      every page in it.  These pages stay mapped across rounds —
    //      pressure accumulates, so each round forces fresh swap-outs.
    //   3. Re-touch hot pages again to surface any swap-in cost
    //      (this is what differentiates a correct clock from FIFO).
    //
    // Total churn at the end of round N is N * CHURN_SZ pages.  After the
    // loop, we release all the churn at once with a single sbrk(-).
    //
    // Why not free each round?  Because the user-process free list would
    // reabsorb the released pages, and round 2 onward would silently
    // reuse them without ever touching swap.  Accumulating pressure is
    // what gives the per-round count a story to tell.
    int churn_total = 0;     // pages successfully grown so far

    for (r = 1; r <= T3_ROUNDS; r++) {
        // Step 1: warm hot pages.
        for (i = T3_HOT_LO; i < T3_HOT_HI; i++)
            sink += mem[PAGE_SIZE * i];

        // Step 2: grow heap by CHURN_SZ pages and touch them.
        churn = sbrk(PAGE_SIZE * T3_CHURN_SZ);
        if (churn == (char *)-1) {
            printf("[round %d] sbrk grow failed; skipping rest\n", r);
            break;
        }
        churn_total += T3_CHURN_SZ;

        for (i = 0; i < T3_CHURN_SZ; i++)
            churn[PAGE_SIZE * i] = (i + r) % 10;
        // Read back so the compiler doesn't elide the writes.
        for (i = 0; i < T3_CHURN_SZ; i++)
            sink += churn[PAGE_SIZE * i];

        // Step 3: re-touch hot.  If clock protected them they're still
        // resident; if FIFO evicted them, this is where swap-in fires.
        for (i = T3_HOT_LO; i < T3_HOT_HI; i++)
            sink += mem[PAGE_SIZE * i];

        swapstat(&x2, &y2);
        printf("[round %d] swap-ins: %d, swap-outs: %d (pages, this round)\n",
               r,
               BLKS_TO_PAGES(x2 - prev_x), BLKS_TO_PAGES(y2 - prev_y));
        prev_x = x2;
        prev_y = y2;
    }

    // Release all accumulated churn pages back to the kernel.
    if (churn_total > 0) {
        if (sbrk(-PAGE_SIZE * churn_total) == (char *)-1) {
            printf("sbrk shrink (cleanup) failed\n");
        }
    }

    // Final integrity check on the resident block.
    for (i = 0; i < NUM_PAGE_T3; i++) {
        if (mem[PAGE_SIZE * i] != i % 10) {
            printf("wrong value error\n");
            wrong++;
        }
    }

    if (wrong == 0)
        printf("final integrity: OK\n");
    else
        printf("final integrity: %d mismatches\n", wrong);

    // Anti-DCE: keep sink alive without printing anything.
    (void)(sink & 0);

    // Verdict
    if (wrong != 0) {
        printf("[Test 3] FAIL - %d wrong values\n", wrong);
    } else if ((prev_y - y1) == 0) {
        printf("[Test 3] PASS (data integrity OK; no swap activity - "
               "use fork_flag=1 or shrink PHYSTOP)\n");
    } else {
        printf("[Test 3] PASS - data integrity preserved\n");
        printf("         NOTE: clock correctness is graded by reviewing the "
               "per-round swap-in trend\n"
               "               (stable across rounds = clock-like, "
               "climbing = FIFO-like)\n");
    }

    free((void *)mem);
}

// ------------- test4: OOM probe + fork-with-swap -------------
//
// Part A: fork() a child that repeatedly calls sbrk(PAGE_SIZE) until it
//         returns -1 (or a safety cap is hit).  The child is the one that
//         drives the system to OOM, so if a printf() call there gets
//         caught by an unrecoverable fault it doesn't take Part B down
//         with it -- the parent just reaps and moves on.
//
// Part B: allocate a small working set with known values, then allocate a
//         larger block to force part of the first set into swap.  fork().
//         Both child and parent verify the original working set
//         independently.
//
// Note: Test 4 ignores the fork_flag argument from argv; it manages its
//       own fork inside each part.

#define T4A_MAX_ITER  20000   // safety cap so the test always terminates
#define T4B_BASE      100     // pages with known values
#define T4B_PRESSURE  200     // extra pages to force swap-out of base

static void
run_test4_partA(void)
{
    int pid;
    int x1, y1, x2, y2;

    swapstat(&x1, &y1);
    printf("[Part A] OOM probe starting\n");

    pid = fork();
    if (pid < 0) {
        printf("[Part A] fork failed\n");
        return;
    }

    if (pid == 0) {
        // Child: probe to OOM. If sbrk fails cleanly, print and exit.
        // If the OOM path crashes us mid-printf, the parent will still
        // reap us and proceed with Part B.
        int i;
        for (i = 0; i < T4A_MAX_ITER; i++) {
            if (sbrk(PAGE_SIZE) == (char *)-1) {
                printf("[Part A] sbrk failed at iter %d (graceful)\n", i);
                exit(0);
            }
        }
        printf("[Part A] sbrk did not fail within %d iterations\n",
               T4A_MAX_ITER);
        exit(0);
    }

    // Parent: wait for the probe child to finish or die.
    wait(0);

    swapstat(&x2, &y2);
    printf("[Part A] swap-ins: %d, swap-outs: %d (pages, this part)\n",
           BLKS_TO_PAGES(x2 - x1), BLKS_TO_PAGES(y2 - y1));
}

// Return value:
//   0  -> PASS (both child and parent verified base integrity)
//   -1 -> SKIP (env failure: malloc or fork could not proceed)
//   N>0 -> FAIL (N = parent's mismatch count; child mismatch printed inline)
static int
run_test4_partB(void)
{
    int i;
    char *base;
    char *pressure;
    int wrong;
    int pid;
    int x1, y1, x2, y2;

    printf("[Part B] fork+swap test\n");

    swapstat(&x1, &y1);

    base = (char *)malloc(PAGE_SIZE * T4B_BASE);
    if (base == 0) {
        printf("[Part B] base malloc failed; skipping\n");
        return -1;
    }

    // Write known values into the base working set.
    for (i = 0; i < T4B_BASE; i++)
        base[PAGE_SIZE * i] = i % 10;

    // Force some of the base pages into swap by allocating and touching
    // a larger block.
    pressure = (char *)malloc(PAGE_SIZE * T4B_PRESSURE);
    if (pressure == 0) {
        printf("[Part B] pressure malloc failed; skipping\n");
        free((void *)base);
        return -1;
    }
    for (i = 0; i < T4B_PRESSURE; i++)
        pressure[PAGE_SIZE * i] = (i + 1) % 10;

    pid = fork();
    if (pid < 0) {
        printf("[Part B] fork failed\n");
        free((void *)pressure);
        free((void *)base);
        return -1;
    }

    if (pid == 0) {
        // Child: verify the inherited base working set.
        // Exit status carries the mismatch count back to the parent so
        // the verdict can include the child's result without relying
        // on output parsing.
        wrong = 0;
        for (i = 0; i < T4B_BASE; i++) {
            if (base[PAGE_SIZE * i] != i % 10)
                wrong++;
        }
        if (wrong == 0)
            printf("[child] integrity: OK\n");
        else
            printf("[child] integrity: %d mismatches\n", wrong);
        // Clamp to 1 byte to fit exit status conventions.
        exit(wrong > 255 ? 255 : wrong);
    }

    // Parent: wait for child first (collect its exit status), then verify own copy.
    int cstatus = 0;
    wait(&cstatus);

    wrong = 0;
    for (i = 0; i < T4B_BASE; i++) {
        if (base[PAGE_SIZE * i] != i % 10)
            wrong++;
    }
    if (wrong == 0)
        printf("[parent] integrity: OK\n");
    else
        printf("[parent] integrity: %d mismatches\n", wrong);

    swapstat(&x2, &y2);
    printf("[Part B] swap-ins: %d, swap-outs: %d (pages, this part)\n",
           BLKS_TO_PAGES(x2 - x1), BLKS_TO_PAGES(y2 - y1));

    free((void *)pressure);
    free((void *)base);

    // Combine: any nonzero mismatch (child or parent) means FAIL.
    if (cstatus != 0 || wrong != 0)
        return (wrong > 0 ? wrong : 1);
    return 0;
}

static void
run_test4(int do_fork)
{
    int partB;

    (void)do_fork;  // Test 4 ignores fork_flag; Part B forks internally.

    printf("zip_test_4\n");

    run_test4_partA();
    partB = run_test4_partB();

    // Verdict.
    // Part A: reaching this point at all means the parent survived the
    // OOM probe child, which implies kalloc returned 0 gracefully rather
    // than panicking the kernel.  That's the Part A success criterion.
    // Part B: integrity of both child and parent.
    if (partB == -1) {
        printf("[Test 4] SKIP - environment could not run Part B "
               "(try restarting xv6 between tests)\n");
    } else if (partB > 0) {
        printf("[Test 4] FAIL - Part B integrity broken "
               "(parent mismatches: %d)\n", partB);
    } else {
        printf("[Test 4] PASS - Part A OOM graceful, Part B integrity OK\n");
    }
}

